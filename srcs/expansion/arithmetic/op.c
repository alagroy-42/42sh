#include "arithmetic.h"

long	ae_sum(long a, long b)
{
	return (a + b);
}

long	ae_sub(long a, long b)
{
	return (a - b);
}

long	ae_mult(long a, long b)
{
	return (a * b);
}

long	ae_div(long a, long b)
{
	return (a / b);
}

long	ae_mod(long a, long b)
{
	return (a % b);
}
